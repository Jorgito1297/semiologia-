import asyncio
import re
from playwright import async_api
from playwright.async_api import expect

async def run_test():
    pw = None
    browser = None
    context = None

    try:
        # Start a Playwright session in asynchronous mode
        pw = await async_api.async_playwright().start()

        # Launch a Chromium browser in headless mode with custom arguments
        browser = await pw.chromium.launch(
            headless=True,
            args=[
                "--window-size=1280,720",
                "--disable-dev-shm-usage",
                "--ipc=host",
                "--single-process"
            ],
        )

        # Create a new browser context (like an incognito window)
        context = await browser.new_context()
        # Wider default timeout to match the agent's DOM-stability budget;
        # auto-waiting Playwright APIs (expect, locator.wait_for) inherit this.
        context.set_default_timeout(15000)

        # Open a new page in the browser context
        page = await context.new_page()

        # Interact with the page elements to simulate user flow
        # -> navigate
        await page.goto("http://localhost:3000")
        try:
            await page.wait_for_load_state("domcontentloaded", timeout=5000)
        except Exception:
            pass
        
        # -> Click the 'Reload' button to reload the login page so the demo login option can be attempted again.
        # Reload button
        elem = page.locator("xpath=/html/body/div/div/div/form/button").nth(0)
        await elem.click(timeout=10000)
        
        # -> Wait for the login page to finish rendering and, if interactive elements appear, click the 'Acceder en Modo Demo Comercial (Prueba Offline)' (Demo) button; if the page remains blank, reload the login page.
        await page.goto("http://localhost:3000/login?next=%2F")
        try:
            await page.wait_for_load_state("domcontentloaded", timeout=5000)
        except Exception:
            pass
        
        # -> Click the 'Acceder en Modo Demo Comercial (Prueba Offline)' button to sign in with the demo account.
        # Acceder en Modo Demo Comercial (Prueba Offline) button
        elem = page.get_by_role('button', name='Acceder en Modo Demo Comercial (Prueba Offline)', exact=True)
        await elem.click(timeout=10000)
        
        # -> Open the Faculty / Analytics page (visit the Faculty Analytics URL) to check whether a non-faculty demo user is shown a restricted-access message and the faculty dashboard is not displayed.
        await page.goto("http://localhost:3000/faculty/analytics")
        try:
            await page.wait_for_load_state("domcontentloaded", timeout=5000)
        except Exception:
            pass
        
        # --> Assertions to verify final state
        current_url = await page.evaluate("() => window.location.href")
        # Assert: page loaded with a URL (final outcome verified by the AI judge during the run)
        assert current_url, 'Page should have loaded with a URL'
        current_url = await page.evaluate("() => window.location.href")
        # Assert: page loaded with a URL (final outcome verified by the AI judge during the run)
        assert current_url, 'Page should have loaded with a URL'
        await asyncio.sleep(5)

    finally:
        if context:
            await context.close()
        if browser:
            await browser.close()
        if pw:
            await pw.stop()

asyncio.run(run_test())
    